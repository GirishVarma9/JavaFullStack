package com.corejava.typecasting;

public class ExplicitCasting {
    public static void main(String[] args) {
        int i = 100;
        byte b = (byte)i;

        int x = 50;
        char ch = (char) x;
        System.out.println(ch);

        int n = 700;
        byte s = (byte) n;
        char k = (char) s;
        System.out.println(k);
    }
}
